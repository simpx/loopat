import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs"
import { extname, join } from "node:path"

const distDir = "web/dist"
const outFile = "server/src/web-assets.ts"

const mimeMap = {
  html: "text/html",
  css: "text/css",
  js: "application/javascript",
  svg: "image/svg+xml",
  png: "image/png",
  json: "application/json",
  ico: "image/x-icon",
  webmanifest: "application/json",
}

function walk(dir, base) {
  const entries = []
  for (const name of readdirSync(dir)) {
    const p = join(dir, name)
    const s = statSync(p)
    if (s.isDirectory()) {
      entries.push(...walk(p, base))
    } else {
      const rel = join(dir.replace(base, ""), name).replace(/^\//, "")
      entries.push({ name: rel, path: p })
    }
  }
  return entries
}

const files = walk(distDir, distDir)

const assets = files.map((f) => {
  const buf = readFileSync(f.path)
  const b64 = buf.toString("base64")
  const ext = extname(f.name).replace(/^\./, "")
  return { name: f.name, b64, mime: mimeMap[ext] ?? "application/octet-stream" }
})

const mimeEntries = [...new Set(assets.map((a) => a.mime))].map((m) => {
  const exts = Object.entries(mimeMap).filter(([, v]) => v === m).map(([k]) => k)
  return exts.map((e) => `"${e}":"${m}"`).join(",")
})

let code = "// auto-generated by build\n"
code += `const _mime: Record<string, string> = {${mimeEntries.join(",")}};\n`
code += "const _assets: Record<string, Uint8Array> = {};\n"
code += `export function serveEmbedded(path: string): Response | null {\n`
code += `  const relPath = path === "/" ? "index.html" : path.slice(1);\n`
code += `  const content = _assets[relPath];\n`
code += `  if (content == null) return null;\n`
code += `  const ext = relPath.split(".").pop() ?? "";\n`
code += `  return new Response(content, { headers: { "content-type": _mime[ext] ?? "application/octet-stream" } });\n`
code += `}\n\n`

for (const a of assets) {
  code += `_assets[${JSON.stringify(a.name)}] = Uint8Array.from(atob(${JSON.stringify(a.b64)}), c => c.charCodeAt(0));\n`
}

// Embed the system prompt template (if exists)
const templateFile = join(distDir, "templates", "sandbox.md")
let templateB64 = ""
try {
  templateB64 = readFileSync(templateFile).toString("base64")
} catch {
  // fallback: use existing template from old web-assets
  try {
    const oldContent = readFileSync(outFile, "utf-8")
    const m = oldContent.match(/getEmbeddedTemplate.*?Buffer\.from\("([^"]+)"/)
    if (m) templateB64 = m[1]
  } catch {}
}

if (templateB64) {
  code += `\n\n// -- embedded templates --\n`
  code += `let _template: string | null = null;\n`
  code += `export function getEmbeddedTemplate(): string | null {\n`
  code += `  if (_template != null) return _template;\n`
  code += `  _template = Buffer.from("${templateB64}", "base64").toString("utf-8");\n`
  code += `  return _template;\n`
  code += `}\n`
} else {
  code += `\n\n// -- embedded templates --\n`
  code += `let _template: string | null = null;\n`
  code += `export function getEmbeddedTemplate(): string | null {\n`
  code += `  if (_template != null) return _template;\n`
  code += `  _template = null;\n`
  code += `  return _template;\n`
  code += `}\n`
}

writeFileSync(outFile, code)
console.log(`Generated ${outFile} (${assets.length} assets)`)
